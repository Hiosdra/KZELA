from miniproject2 import pPlusQ, pPlusP


# task
def nP(A, p, n, x, y):
    Q = (x, y)
    R = (0, 0)
    while n > 0:
        if (n % 2) == 1:
            R = pPlusQ(Q[0], Q[1], R[0], R[1], p)
            n = n - 1
        Q = pPlusP(A, Q[0], Q[1], p)
        n = n / 2
    return R


